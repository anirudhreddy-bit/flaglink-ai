/** History / legacy session payloads may omit `flags` and use `redFlags` + string[] advice */
function normalizeScanResult(raw: unknown): ScanResult {
  const r = raw as Partial<ScanResult> & { redFlags?: RedFlag[] };

  let flags: Flag[];
  if (Array.isArray(r.flags)) {
    flags = r.flags;
  } else if (Array.isArray(r.redFlags)) {
    flags = r.redFlags.map((f) => {
      const s = String(f.severity);
      const severity = (s === "critical" ? "critical" : s === "high" ? "high" : s === "low" ? "low" : "medium") as Flag["severity"];
      return {
        id: "LEGACY",
        title: f.clause,
        description: f.explanation,
        severity,
        quote: "",
      };
    });
  } else {
    flags = [];
  }

  let advice: AdviceTip[];
  if (!Array.isArray(r.advice)) {
    advice = [];
  } else if (r.advice.length > 0 && typeof (r.advice as unknown[])[0] === "string") {
    advice = (r.advice as unknown as string[]).map((tip) => ({ flag_id: "", tip }));
  } else {
    advice = r.advice as AdviceTip[];
  }

  const safe_clauses = Array.isArray(r.safe_clauses) ? r.safe_clauses : [];

  return {
    domain: r.domain ?? null,
    score: typeof r.score === "number" ? r.score : null,
    verdict: r.verdict ?? null,
    summary: r.summary ?? null,
    flags,
    advice,
    safe_clauses,
    error: r.error ?? null,
    riskLevel: r.riskLevel,
    redFlags: r.redFlags,
  };
}

// ── Severity helpers ──────────────────────────────────────────────────────
const severityColor = (s: string) => {
  if (s === "critical") return "#dc2626";
  if (s === "high")     return "#ea580c";
  if (s === "medium")   return "#d97706";
  return "#6b7280";
};
const severityBg = (s: string) => {
  if (s === "critical") return "#fef2f2";
  if (s === "high")     return "#fff7ed";
  if (s === "medium")   return "#fffbeb";
  return "#f9fafb";
};

// ── Verdict styling ───────────────────────────────────────────────────────
const verdictStyle = (v: string | null) => {
  if (v === "Critical Risk") return { bg: "#fef2f2", text: "#dc2626", border: "#fecaca" };
  if (v === "High Risk")     return { bg: "#fff7ed", text: "#ea580c", border: "#fed7aa" };
  if (v === "Medium Risk")   return { bg: "#fffbeb", text: "#d97706", border: "#fde68a" };
  return { bg: "#f0fdf4", text: "#16a34a", border: "#bbf7d0" };
};

