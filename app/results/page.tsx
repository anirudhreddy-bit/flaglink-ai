function sortFlagsBySeverity(flags: Flag[]): Flag[] {
  return [
    ...flags.filter((f) => f.severity === "critical"),
    ...flags.filter((f) => f.severity === "high"),
    ...flags.filter((f) => f.severity === "medium"),
    ...flags.filter((f) => f.severity === "low"),
  ];
}

async function downloadReportPdf(opts: {
  domain: string;
  score: number | null;
  verdict: string | null;
  summary: string | null;
  flags: Flag[];
}) {
  // Use browser bundle explicitly — Next/Turbopack otherwise resolves the Node build and fails on `fflate`.
  const { jsPDF } = await import("jspdf/dist/jspdf.es.min.js");
  const doc = new jsPDF();
  const margin = 14;
  let y = 18;
  const lineHeight = 6;
  const pageH = doc.internal.pageSize.getHeight();
  const maxW = doc.internal.pageSize.getWidth() - margin * 2;

  const ensureSpace = (needed: number) => {
    if (y + needed > pageH - 16) {
      doc.addPage();
      y = 18;
    }
  };

  const addLines = (text: string, size = 10, gapAfter = 3) => {
    doc.setFontSize(size);
    doc.setTextColor(0, 0, 0);
    const lines = doc.splitTextToSize(text, maxW);
    for (const line of lines) {
      ensureSpace(lineHeight);
      doc.text(line, margin, y);
      y += lineHeight;
    }
    y += gapAfter;
  };

  doc.setFontSize(15);
  doc.text("FlagLink AI — Terms report", margin, y);
  y += 10;

  addLines(`Source: ${opts.domain}`, 11);
  addLines(`Score: ${opts.score ?? "—"} / 100`, 11);
  addLines(`Verdict: ${opts.verdict ?? "—"}`, 11);
  if (opts.summary?.trim()) addLines(`Summary\n${opts.summary.trim()}`, 10, 5);

  addLines("Red flags", 12, 2);
  const sorted = sortFlagsBySeverity(opts.flags);
  if (sorted.length === 0) {
    addLines("No flags listed for this scan.", 10);
  } else {
    for (const f of sorted) {
      const block = `[${String(f.severity).toUpperCase()}] ${f.title}${f.description ? `\n${f.description}` : ""}${f.quote ? `\nQuote: ${f.quote}` : ""}`;
      addLines(block, 9, 4);
    }
  }

  doc.setFontSize(8);
  doc.setTextColor(90, 90, 90);
  addLines(
    "This report is generated by FlagLink AI for informational purposes only. It is not legal advice.",
    8,
    0
  );

  const safeName = opts.domain.replace(/[^\w.-]+/g, "_").slice(0, 48) || "report";
  doc.save(`flaglink-report-${safeName}.pdf`);
}
